CREATE TABLE sector
(
    id   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(MAX) NOT NULL,
    parent_id BIGINT REFERENCES sector(id),
    order_nr INT NOT NULL,
    UNIQUE (parent_id, order_nr)
);

CREATE TABLE user_data
(
    id      UUID PRIMARY KEY,
    name    VARCHAR(MAX) NOT NULL,
    agreed_tos BOOLEAN NOT NULL
);

CREATE TABLE user_data_sector
(
    form_data_id UUID NOT NULL REFERENCES user_data (id),
    sector_id    BIGINT NOT NULL REFERENCES sector (id),
    PRIMARY KEY (form_data_id, sector_id)
);

